                <h2>Goals and Objectives </h2>
                <hr class="brief--hr">

                <p>
                    Video Advertising is classified according to different criteria. For example, according to the intended purpose, it can be of three types.
                    <br>
                    <li> Social advertising. Such advertising is aimed at changing the behavior of the public. It draws attention to an issue.</li>
                    <li> Image advertising. Helps to increase brand awareness, create the right associative range and fix the image of the company in the minds of consumers.</li>
                    <li> Commercial advertising. It is aimed at selling goods and services, as well as promoting the company on the market.</li>
                    <br>
                    To set a goal, you need to determine the criteria for your video and what kind of video you need.

                    Based on the content format, video ads are classified into two main types.
                    Staged. Most often, it involves the implementation of an advertising script in the form of a mini-format film. However, it may look like an appeal from a specialist, a monologue, a fragment of an interview.
                    Animated. Such advertising can look like a cartoon, motion graphics, puppet animation, and so on.
                    <br>
                    There is also an intro video that is inserted at the very beginning of other videos as a preface or greeting.
                    <br>
                    Based on promotion channels, video advertising is divided into television, Facebook, YouTube, Linkedin, TikTok and others.
                    <br>
                    The tasks are divided into three stages - pre-production, production and post-production.
                    Tasks include developing a script, recording a voiceover and shooting a video, or developing a motion design, depending on the goals of your video.
                    At the pre-production stage, references are selected, a concept is developed, a script is written, and a budget is formed.
                    Production is already the direct creation of the video itself.
                    Post-production is the finalization of the details of the video, depending on the type of video, this can be music, sound effects or finalizing the video to the desired results using motion design.
                </p>



