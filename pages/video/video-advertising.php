                <h3>There are several types of video advertising techniques that can be used to promote products or services. Some of the most common include:</h3>
                <ol>
                    <li> Pre-roll advertising: This type of video advertising appears before the main content of a video, usually on platforms like YouTube or Vimeo. It's a good option for reaching large audiences and targeting specific demographics.</li>

                    <li> In-stream advertising: This is a similar to pre-roll advertising but it appears during the video content, interrupting the flow of the video. It's also commonly used on platforms like YouTube and Vimeo.</li>

                    <li> Post-roll advertising: This type of video advertising appears after the main content of the video. It's a good option for targeting viewers who have already engaged with the main content.</li>

                    <li> Outstream advertising: This type of video advertising is viewed on webpages or mobile apps and it's not necessarily linked to any video content.</li>

                    <li> Skippable video ads: This video format allows users to skip the ad after a certain amount of time, usually 5 seconds, giving the user a choice to engage or skip the ad.</li>

                    <li> Bumper video ads: Short video ads that are between 6-10 seconds long, they are non-skippable. They are often used to create brand awareness and recall.</li>

                    <li> Interactive video ads: Video ads that allow the viewer to interact with the ad, for example, by clicking on links or buttons, filling out forms, or making purchases.</li>

                    <li> Native video ads: This type of video advertising is integrated seamlessly into the platform it's displayed on, it's often used to create a more organic and non-intrusive experience.</li>
                </ol>
                <p> Each of these video advertising techniques has its own unique benefits and can be used in different ways depending on the goals and target audience of a campaign.




                </p>



