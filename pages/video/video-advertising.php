                <h2> Script</h2>
                <hr class="brief--hr">

                <p>
                    Scripting a promotional video is much more difficult than writing a short film or feature film. Indeed, here, in addition to writing talent, deep knowledge of marketing, study of the target audience and, in general, an understanding of consumer psychology will be required.
                    In the world of cinematography, there have been strict rules about the scripts that all industry professionals work for a very long time. And these rules apply not only to cinema. Competent writers of video production studios use these rules in their work in the same way.
                    Each structural element in the scenario has its own design type. Each scene opens under its own serial number. The type of location (interior / exterior), the name of the location and the time of day (morning, afternoon, evening, night) are indicated. Under the scene opening is a brief description of the action - what happens in the scene. Without unnecessary adjectives, comparisons, descriptions. Character names are in capital letters. The direct speech of the character is located in the center under the direct action. Notes are highlighted on a separate line in capital letters. Remarks - under the names of the characters, in brackets.

                </p>

