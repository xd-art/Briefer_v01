                <h1 style="font-family: 'Fjalla One', sans-serif;">Max Tegmark on the Nature of Learning: How Brains Evolve to Think and How AI Follows Suit</h1>



                <hr class="brief--hr">
                <p>After reading Max Tegmark's book "<i>Life 3.0</i>", I discovered an intriguing classification of intellectual development that the author proposes and calls "<strong>lives</strong>". This concept provides an interesting perspective on the evolution of artificial intelligence and its impact on the future of humanity.</p>

                <p>At the first level, "<strong>Life 1.0</strong>", intellect at the level of biological organisms such as bacteria that act according to predetermined programs and instincts is considered.</p>

                <p>At the level of "<strong>Life 2.0</strong>", which is characteristic of modern humans, we see the possession of the ability to learn, create new ideas, as well as the possession of consciousness and self-awareness.</p>

                <p>However, the most exciting part is "<strong>Life 3.0</strong>", implying the onset of the era of superintelligence. This is the level at which intellect is capable not only of learning but also of independently influencing its development and environment. Here, questions arise about how to ensure the safe and ethical development of such intelligence, as well as how it can affect the self-determination of humanity.</p>



                <img class="--image" src="/images/pictures/blog/max-tegmark-1.png" alt="">




                <p>After carefully reading the book, the reader encounters an interesting consideration of the learning process proposed by the author. The text raises the question of how physical systems, including artificial neural networks and the human brain, are able to learn and adapt to a changing environment.</p>

                <p>Although a pocket calculator, as the author notes, can surpass a person in the speed of performing arithmetic calculations, it is not able to improve its performance in the course of practice. This contrast is emphasized by the example of a chess program, which, although it defeats a person in chess, cannot learn from its mistakes and performs the same programmed actions every time it evaluates the next move.</p>

                <p>The ability to learn is considered a fundamental characteristic of strong intelligence. We have already seen how a seemingly meaningless piece of lifeless matter turns out to be able to remember and calculate, but how can it learn? We have seen that finding the answer to a difficult question involves calculating some functions, and suitably organized matter can calculate any computable function. When we humans first created pocket calculators and chess programs, we somehow organized matter in a certain way. And now, in order to learn, this matter simply must, following the laws of physics, reorganize itself, becoming better and better at calculating the necessary functions.</p>






