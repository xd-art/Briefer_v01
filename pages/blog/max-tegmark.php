                <p>To unravel the learning process, let's first look at how a very simple physical system can learn to calculate the sequence of digits of the number π or any other number. We have seen earlier how a hilly surface with many hollows between hills can be used as a storage device: for example, if the x-coordinate of one of the hollows exactly matches π, and there are no other hollows nearby, then by placing a ball at the point with x-coordinate 3, we can see how our system calculates the missing digits after the decimal point, simply by watching how the ball rolls into the hollow.</p>

                <p>Now suppose the surface is made of soft clay, originally completely flat like a table. But if some math enthusiasts keep placing balls at the same points with coordinates corresponding to their favorite numbers, hollows will gradually form at these points due to gravity, and over time this clay surface can be used to find out which numbers it has "remembered". In other words, the clay has learned to calculate significant digits of the number π.</p>

                <p>Other physical systems, including the brain, can learn much more efficiently, but the idea remains the same. John Hopfield showed that his network of intersecting neurons mentioned above can store many complex memories by simple repetition. This kind of information representation for learning is usually called "<strong>training</strong>" when it comes to artificial neural networks (as well as animals or humans who need to acquire a certain skill), although words like "<strong>experience</strong>", "<strong>upbringing</strong>" or "<strong>education</strong>" also fit.</p>

                <p>In the artificial neural networks that underlie modern artificial intelligence systems, Hebbian learning is usually replaced by more complex rules with less melodious names, such as "<strong>backpropagation</strong>" or "<strong>stochastic gradient descent</strong>", but the basic idea remains the same: there is some simple deterministic rule, similar to the law of physics, according to which synapses are updated over time. As if by magic, using this simple rule, a neural network can be trained to perform extremely complex computations if large data sets are used for training. We still do not know exactly what rules the brain uses for learning, but whatever the answer, there is no evidence that these rules violate the laws of physics.</p>


                <p>Most digital computers increase their efficiency by breaking down tasks into multiple steps and reusing the same computational modules over and over, and artificial and biological neural networks do the same. The brain has areas that in computer science are called "<strong>recurrent neural networks</strong>": information in them can move in different directions, and what served as output at the previous stage can become input at the next one - this distinguishes them from feedforward.</p>

                <p>The logic gate network in a laptop microprocessor is also recurrent in this sense: it continues to use already processed information while allowing new information to enter - from the keyboard, trackpad, camera, etc., which also has the right to influence the current calculations, which in turn determine how information will be output: to the monitor, speakers, printer or via wireless network. Similarly, the neural network in your brain is recurrent because it receives information from your eyes, ears, and other sense organs, and allows this information to influence the current calculations, which in turn determine how the results will be output to your muscles.</p>

                <p>The history of learning is as long as the history of life itself, since every self-reproducing organism somehow copies and processes information, that is, had to learn how to behave. However, in the era of "<strong>Life 1.0</strong>", organisms did not learn during their lifetime: the ways of processing information and responding were determined by the DNA passed on to the organism, so learning took place slowly at the species level through Darwinian evolution from generation to generation.</p>





                <p>About half a billion years ago, some gene lines here on Earth paved the way for the emergence of animals with neural networks, giving such animals the ability to change their behavior by learning from experience during their lifetime. When <strong>Life 2.0</strong> appeared, it won the competition of species and spread across the planet like wildfire thanks to its ability to learn much faster. In the first chapter, we already learned that life has gradually improved its ability to learn, and has done so at an ever-increasing rate.</p>

                <p>In one species of primates, the brain turned out to be so well adapted to learning that they learned to use various tools, speak, shoot, and created an advanced society that spread around the world. This society itself can be seen as a system that remembers, calculates, and learns, and it does all this at an ever-increasing rate, since one invention leads to another: writing, printing, modern science, computers, the Internet, and so on. What will be the next inventions that future historians will add to this list of accelerating learning?</p>

                <p>As we all know, the avalanche of technical achievements that improve computer memory and increase computing power has led to impressive advances in artificial intelligence, but it took machine learning a long time to mature. When IBM's Deep Blue computer defeated world chess champion Garry Kasparov in 1997, its main advantages were memory and the ability to calculate quickly and accurately - not the ability to learn. Its computational intellect was created by a team of people, and the key reason why Deep Blue was able to defeat its creators was its ability to calculate faster, which allowed it to analyze more new positions on the chessboard. When IBM's Watson computer surpassed the human who was the strongest in the game of Jeopardy!, it too relied not on learning but on specially programmed skills and superior memory and speed. The same can be said for all breakthrough technologies in robotics, from self-balancing vehicles to unmanned cars and rockets that can automatically land.</p>






                <p>In contrast, machine learning has been the driving force behind many recent advances in artificial intelligence. Look, for example, at this photo. You will instantly guess what it shows, but writing a function that takes the color of each pixel of the image as input and gives an accurate description of the photo as output, for example: "A group of young people playing frisbee," took decades of effort from artificial intelligence researchers around the world. And only Google's team managed to do it in 2014. If you enter another set of pixels, the output will be: “A herd of elephants walking across a dry grassy field” - the answer will again be accurate. How did they do it? By manual programming, as Deep Blue did, creating each algorithm that recognizes frisbee play, faces, and the like separately? No, they created a relatively simple neural network that initially had no knowledge of the physical world and its components, and then allowed it to learn by providing it with a huge amount of information.</p>

                <p>In 2004, famous publisher Jeff Hawkins wrote about artificial intelligence: "No computer can... see as well as a mouse," but those days are long gone.</p>




                <img class="--image" src="/images/pictures/blog/max-tegmark-2.png" alt="">


                <p>"A group of people playing frisbee" - the caption generated by this machine for this photo, which knew nothing about people, games, or frisbees.</p>




                <p>Just as we do not fully understand how our children learn, we still do not fully understand how these neural networks learn and why they sometimes fail. But it is already clear that they will be very useful, which is why deep learning has attracted investors. Thanks to deep learning, approaches to the technical implementation of computer vision have radically changed: from handwriting recognition to real-time video streaming analysis and driverless cars. Thanks to it, there was a revolution in the use of a computer to convert speech to text and translate it into other languages, even in real time, so now we can talk to personal digital assistants like Siri, Google Now, or Cortana. Annoying CAPTCHA puzzles that we have to solve to convince a site that we are human are becoming more and more difficult to outperform machine learning technologies. In 2015, Google DeepMind released an artificial intelligence that used deep learning to master dozens of different computer games just like a child - that is, without using instructions, with the only difference being that it learned to play better than any human.</p>

                <p>In 2016, the same company released AlphaGo, a computer system for the game of Go, which used deep learning to evaluate positional advantages so accurately that it defeated the world's strongest player. This success serves as positive feedback, attracting more funding and more talented youth to artificial intelligence research, leading to new successes.</p>





                <p>How long will it take machines to surpass us in solving all cognitive tasks? We don't know, and we must be prepared for the answer to be "never." However, the goal is to prepare for it to happen anyway, perhaps even in our lifetime. Because matter can be organized in such a way that, obeying the laws of physics, it remembers, calculates, and learns - and matter does not necessarily have to be biological.</p>

                <p>Artificial intelligence researchers are often accused of over-promising and under-delivering, but in fairness, many such critics also have dull records. Some of them are just playing with words, at one point defining intelligence as something computers still cannot do, and then as something that will impress us the most. Computers have now become very good, or even superior in arithmetic, chess, mathematical theorem proving, stock picking, pattern recognition, driving, arcade games, go, speech synthesis, speech-to-text conversion, translation, and even cancer diagnosis, but another critic just snorts contemptuously: "Of course, you don't need a real mind for that!" - and will continue to argue that real intelligence must reach the peaks of Moravec's landscape that are not yet submerged in water, like those people in the past who argued that a machine was not up to subtitles or go - while the water continued to rise.</p>

                <p>Assuming the water will continue to rise for at least some time, it is reasonable to assume that the impact of artificial intelligence on society will continue to grow.</p>

                <p>Deep learning has become a driver of technical progress in various fields. Computer vision, speech recognition, and language translation processes have moved to a new level thanks to this technology. The ability of machine learning to quickly adapt to new data and scenarios has led to the creation of systems capable of recognizing objects in real time, giving rise to driverless cars and smart homes.</p>

                <p>Projects like Google DeepMind and AlphaGo demonstrate how deep learning leads to the creation of artificial intelligence capable not only of adapting but also of surpassing human abilities in complex tasks such as the game of go. These achievements attract the attention of investors and talented researchers, stimulating further progress in the field of artificial intelligence.</p>

                <p>The author also draws attention to the discussions in the field of artificial intelligence, where critics often raise questions about how successful artificial intelligences have already become in various areas, but at the same time continue to challenge their true intellectual status. He warns against a dismissive attitude towards the achievements of artificial intelligence and notes that it is necessary to recognize their successes, even if they differ from what we usually associate with human intelligence.</p>

                <p>Max Tegmark's book "<b>Life 3.0</b>", published in 2019, left a deep mark on our perception of artificial intelligence. Since then, a few years have passed, and during this time the integration of artificial intelligence into everyday life has only intensified.</p>

                <p>Natural language processing systems like ChatGPT have become more sophisticated and widely used to enhance human-computer interaction. Google Bard and Microsoft Bing, backed by advanced artificial intelligence technologies, continue to improve search query quality and provide more accurate results.</p>


                <p>All these changes emphasize the constant growth of the influence of artificial intelligence on our daily lives. It not only transforms the ways we interact with technology but also becomes an indispensable tool in various fields, enriching and improving the quality of our existence.</p>
















