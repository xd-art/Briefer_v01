                <h1>Marketing Case by Nike Company</h1>
                <h2 class=""> <b>&#x2022 Can the Sports Equipment Industry Leader Nike Disrupt Baseball Gloves and Hockey Sticks in the $40B Market?</b> </h2>



                <hr class="brief--hr">



                <p>
                    Sitting at his desk buried under a pile of brown baseball gloves, John Schult of Nike rummaged through the leather mound and nimbly extracted something indefinite. It looked like a miniature rubber excavator scoop with a mesh glove attached to the back. Schult waved the scoop from side to side and held it out to a group of Nike's senior managers, who eyed the suspicious object warily. He explained that the rubber scoop was a prototype for a baseball glove that the company planned to bring to market in early 1999.

                    <br> <br> <b>An experimental prototype:</b> he hastened to add, fully aware that no professional baseball player, no matter the value of his Nike endorsement deal, would ever wear something that looked more like an alien's garment. "The final model will look completely different. That's what our people are diligently working on right now as we speak."
                </p>

                <img class="--image" src="/images/pictures/blog/Leonardo_Vision_XL_A_Nike_baseball_glove_that_looks_like_an_al_1.jpg" alt="">


                <p>
                    Just imagine: Nike, the industry leader in athletic shoes and apparel whose swoosh logo is as recognizable as the SOS sign, is bursting into the athletic equipment market. Nike's experts have delved into the minutiae of baseball gloves and bats, hockey sticks and soccer balls, snowboards and golf balls. They're trying to incorporate aerospace and defense technology into the manufacturing process, using materials from those industries. The driving idea is not just to make and sell equipment but to develop something unseen before, and use Nike's marketing machine to force it into the market and convince the world of its technological superiority. According to Andrew Mooney, who heads Nike's equipment division, the next few years will see the highest growth rates in the company's equipment sector. At the same time, he promises, <br> <br><b style="font-size: 33px;"" >" we won't grasp at everything we could improve."</b>
                </p>

                <p>
                    The plan is audacious, but well within the capabilities of a company that has convinced people to pay hundreds of dollars for a pair of flashy sneakers that tally $9.2 billion in annual sales. The difficulty of the task is acknowledged by Mooney, a company veteran in marketing. <br><br> <b>"If someone had told me three years ago that I would be marketing hockey and baseball equipment, snowboards and roller blades, and stuff like that, all at once, I would have said it was pure madness,"</b> he says. Or at the very least, unprecedented daring.
                </p>



                <img class="--image" src="/images/pictures/blog/OIG.jpeg" alt="">


                <p>
                    The athletic equipment market, though somewhat diverse, boasts tremendous capacity. Its annual sales volume is about $40 billion, exceeding that of athletic footwear ($20 billion) and apparel ($38 billion). In addition, Nike has to catch up to its main rivals in footwear and apparel. Italy's Fila Holdings SpA has its own roller blade brand. Reebok International Ltd makes soccer balls and exercise equipment. Last fall, Germany's Adidas AG spent $1.3 billion to acquire Salomon SA, the French sporting goods maker, and now controls 22% of the ski equipment and golf accessory markets. On top of that, large equipment companies like Rawlings Sporting Goods and Wilson Sporting Goods, a division of Finland's Amer Group Ltd, have no intention of relinquishing market share without a fight. <b>"Our customers demand top quality goods, not Michael Jordan,"</b> says John Rangel, vice president of K2 Inc, which makes winter sports and roller blade equipment. He and many others believe Nike will probably be unable to maintain a foothold in the sporting goods market, where profitability levels are by no means excessive. <b>"Gloves aren't sneakers when it comes to sales,"</b> notes Sebastian Dicasoli, director of marketing for the manufacturing association.
                </p>

                <p>
                    While retail markups on sneakers and athletic shoes can exceed 50% over cost, baseball glove markups hover around 30%. Moreover, some of Nike's early sporting forays were literally disastrous. At a July meeting in Chicago, Nike organized roller blading demonstrations by some roller bladers outside the meeting venue. The festivities were marred as the skates began falling apart one after another. According to Mooney, <b>"it was a quick and good lesson for us."</b> He attributes all such failures to growing <br><br> <b style="font-size: 33px;">"This division is still in its infancy. Twenty-five years ago there were complaints about our shoe quality, and five years ago about our clothing quality. Listen to what they'll say about our equipment five years from now."</b>
                </p>

                <p>
                    Despite these ambitious plans, Nike's investment in equipment development to date has been fairly modest compared to that of its rivals. For example, management plans to spend no more than $500,000 developing a new baseball glove. At the same time, according to Mooney, with this glove Nike plans to <b>"catch" $200 million annually.</b> <b>"When it comes to conservative goods, a baseball glove has always been made of brown leather. Ours will be completely different,"</b> he says.
                </p>

                <p>
                    Nike's specialists began studying the glove 18 months ago. They started their research by surveying 200 lower league and college players about deficiencies in the traditional baseball glove (according to Schult, major league players have played baseball for too long to notice such things). Currently, Nike's task seems to have taken shape. <b>"Most guys tell us their gloves are fine,"</b> says Robert Bail, a Nike product developer. According to industry experts, Nike could hardly have picked a more difficult task than improving the baseball glove. Any innovations in baseball equipment, other than perhaps the aluminum bat, are doomed to failure, as was the case with Rowlings, which offered baseball gloves with inflatable air bladders for fielders in the early 1990s. The expectation was that this glove, equipped with a miniature pump, would replicate the success of Reebok's Pump sneakers. But according to Chuck Malloy, Rowlings' manager, the endeavor was a total failure, because baseball players are generally averse to any <b>"gimmicks."</b> The game of baseball itself is extremely conservative. <br><br> <b style="font-size: 33px;">"That's why they still play with white balls,"</b> notes Dicasoli, director of marketing for the manufacturing division. And baseball gloves, their smell, texture, the way generations of children have nurtured them, applying treatments and bending them into shape – this is perhaps the most traditional element of the game.
                </p>


                <p>
                    In the search for fresh ideas, Nike brought in some outside experts to help develop a new glove. According to Schult, along with the desire to offer something new, the company respects baseball traditions. At this stage, the prototype glove developed by Nike's experts is proudly referred to as a "hand terminal." However, they did manage to identify one potential deficiency in the traditional leather glove. According to Bail, <b>"players spend a lot of time breaking in their glove because leather is a fairly rigid material. Therefore, we decided:</b> since our glove isn't made of leather, the break-in time will be minimal. It's also more flexible and lighter than a traditional glove." Nike's prototype weighs less than 300 grams, whereas a traditional leather glove tips the scales at nearly a kilogram. Instead of laces, it uses plastic buttons and wire loops. The rubber scoop sits on the hand like a well-worn leather glove. Schult says they still haven't decided whether to use natural leather for the inner lining of the glove. He says the new glove currently smells like glue rather than leather.
                </p>

                <p>
                    In addition to the glove, Nike has developed an aluminum baseball bat with excellent performance characteristics to be launched next fall. While most aluminum bats are made from aerospace scrap, Nike collaborated with France's Pechiney SA, a metals and alloys specialist, on its new bat. This company developed an alloy that facilitates greater ball swerve on impact, which significantly increases flight distance. Mooney contends that <b>Nike's new bats will outperform competitors' models.</b>
                </p>


                <img class="--image" src="/images/pictures/blog/Leonardo_Vision_XL_design_infographic_for_this_information_The_2.jpg" alt="">

                <p>
                    Nike's ambitious plans to conquer the baseball equipment market worry Rawlings, the industry leader with a 110-year history. Under its previous owner, Figgie International, Rawlings was so confident that it did not even consider changing anything in its football gear when competitors offered lightweight urethane shoulder pads for football in the mid-1980s. <b>"The competitors simply raced past us back then, leaving us behind,"</b> says Randy Black, Rawlings' vice president of marketing. Now Rawlings claims it will not allow anything similar to happen with its baseball gear. The company is already taking swings at Nike, calling its new bats iron bludgeons, and offering its own high-performance aluminum bats. The company is also promoting its series of <b>"individual" gloves</b> – next season some New York Yankees players will take the field in Rawlings' new pinstriped gloves. Rawlings has signed baseball star Ken Griffey Jr. of the Seattle Mariners. Next year he will play in a custom lightweight Rawlings glove (in addition to payment for wearing the new glove, Griffey will receive royalties for each glove sold in that model). This contract is also a jab at the competitor, because Ken Griffey Jr. advertises Nike shoes and apparel. Next year Rawlings also plans to diversify its product range. The company intends to offer baseballs with a built-in microchip that calculates flight time from the pitcher's throw until the ball is caught by the catcher. The ball's surface display will show the player flight speed calculated using throw time and distance. <br><br> <b style="font-size: 33px;">"Innovation and tradition are not mutually exclusive to some extent,"</b> acknowledges Black. <b>"And you know what? The swoosh looks like a sneer, suggesting that if you get too big for your boots, Nike will crush you."</b>
                </p>


