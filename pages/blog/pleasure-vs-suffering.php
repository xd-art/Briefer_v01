                <h1><span style="font-weight: 700; font-family: 'Fjalla One', sans-serif; font-size: 60px;"> Pleasure vs Suffering: <br> </span> What Influences Purchase Decisions from a Neuromarketing Perspective</h1>






                <hr class="brief--hr">

                <br><br>


                <div style="padding-right: 10px; float:left;">

                    <h2 style="font-family: 'Fjalla One', sans-serif;"> Neuromarketing <br> Research</h2>
                </div>




                <p>According to neuromarketing research, purchase decisions in our brain are based on the ratio between pleasure and suffering. Experiments using <a href="https://en.wikipedia.org/wiki/Functional_magnetic_resonance_imaging" target="_blank">functional magnetic resonance imaging (fMRI)</a> have shown that seeing a desired product activates the so-called "pleasure center" in the brain. This is the area of the brain responsible for rewards and satisfaction. The stronger the pleasure center is activated by the product, the more we want it.</p>


                <p>However, when we see the price for the same product, a completely different area is activated in the brain - the "suffering center." It is responsible for negative sensations and pain. Essentially, our brain interprets the price as suffering from having to part with money. It is the ratio between the activity of the pleasure center and the suffering center that ultimately determines whether a purchase will be made or not. If pleasure outweighs the pain of the price - we are more likely to buy the product.</p>

                <p>Thus, studying the activity of different brain areas gives us an understanding of how our purchasing decisions are made at the deepest level. This knowledge can be used for more effective marketing and advertising.</p>



                <div style="padding-right: 10px; float:left;">

                    <h2 style="font-family: 'Fjalla One', sans-serif;"> Endowment <br> Effect </h2>
                </div>




                <p>By examining other research in behavioral economics, an interesting relationship was found between the described mechanism of making purchasing decisions and the <a href="https://en.wikipedia.org/wiki/Endowment_effect" target="_blank">endowment effect</a>. There are interesting parallels between the endowment effect and how the brain makes purchasing decisions based on the ratio of pleasure and suffering.</p>


                <img class="--image" src="/images/pictures/blog/Pleasure-vs-Sufferin.png" alt="">




                <h2 style="font-family: 'Fjalla One', sans-serif;">Endowment Effect and the Suffering Center</h2>
                <p>The endowment effect is that people place greater value on what they already have compared to what they don't have. We don't want to lose what we consider ours. This resonates with the activation of the "suffering center" when seeing the price of a product.</p>

                <p>At a subconscious level, we associate the price with suffering from losing money that is already our property. The higher the price, the more the suffering center is activated. At the same time, the endowment effect can enhance the activity of the "pleasure center" from the product itself.</p>

                <p>If this product is already our property, we get additional pleasure from owning it. Thus, the endowment effect increases the gap between pleasure from the product and suffering from the price. This makes us more likely to keep the product rather than part with it.</p>

                <h2 style="font-family: 'Fjalla One', sans-serif;">Diminishing Marginal Utility of Wealth</h2>
                <p>In addition, there is another economic effect that also resonates with the described brain activity in making purchasing decisions. This is the effect of diminishing marginal utility of wealth.</p>

                <p>The diminishing marginal utility of wealth can indeed be juxtaposed with how the brain reacts to price when making purchasing decisions. In both cases, a similar pattern is observed.</p>

                <p>As wealth increases, the utility of each subsequent unit (dollar, euro, etc.) decreases - this is the diminishing marginal utility. Similarly, as fMRI experiments showed, as the price of a product increases, the activity of the "suffering center" in the brain intensifies, but each subsequent price increase causes a smaller increase in the activity of this center.</p>

                <p>That is, the marginal suffering from the price also diminishes. A difference of $10 bothers us more when the price of a product is $50 vs $60 than when it is $500 vs $510. This similarity is not accidental. In both cases, the Weber-Fechner psychophysical law applies: human sensations are proportional to the logarithm of the actual stimulus.</p>



