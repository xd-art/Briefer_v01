                <h2 style="font-family: 'Fjalla One', sans-serif;">Who Created YouTube</h2>

                <p>YouTube creators are three young guys from the so-called PayPal mafia: Chad Hurley (was PayPal's chief designer), Jawed Karim (was PayPal's anti-fraud system developer), Steve Chen - computer engineer. After eBay bought the PayPal company, Steve and Jawed got rich and wanted to create their own project.</p>

                <p>A little background on YouTube's creators:</p>

                <ul>
                    <li><strong>Chad Meredith Hurley.</strong> Born in 1977. A native of Reading, Pennsylvania. As a child, Chad was fond of drawing, in high school he immersed himself in the field of IT technologies. He graduated from the College of Visual Arts. Created the first PayPal logo, after which he was hired there.</li>
                    <li><strong>Steven Chen.</strong> Born in 1978. Born in Taiwan, where he graduated from National University, then moved to America, where he graduated from the School of Mathematics, then successfully graduated from the largest campus of the University of Illinois. After studying, he also worked at PayPal. At the time of creating YouTube, he had experience owning his own company (Chen Systems, a supercomputer manufacturing company he sold in 1996).</li>
                    <li><strong>Jawed Karim.</strong> Born 1979. Born in GDR, then moved with his family to FRG, and at the age of 13 - to the USA. He graduated from Central School in Minnesota, enrolled at the same University of Illinois, but did not complete his studies, got a job at PayPal. His parents are scientists. Even while in high school, the guy became the creator of an autonomous electronic messaging system.</li>
                </ul>

                <p>The idea of ​​creating a video hosting allegedly belonged to Jawed Karim. It came up after the 2004 Indian Ocean earthquake, which caused a tsunami. Jawed wanted to find video of this event on the Internet but didn't find any. Then he voiced his idea of ​​creating a hosting for videos on any topic to his friends. It turned out that his friends were also enthusiastic about the idea of ​​creating something similar. They failed to send a video from one of the parties to their friends, which led to the idea of ​​creating such a service.</p>

                <p>But the idea of ​​creating a hosting for videos on any topic seemed too abstract to the guys, so it was decided to launch the service as a dating site, which were gaining popularity in those years. This is how YouTube's history began.</p>

                <p>In general, there are different versions regarding the idea of ​​creating video hosting. We will not insist on any of them. Users no longer care so much about who exactly owns the idea. In the end, the guys worked together, and we enjoy using their brainchild with pleasure.</p>

                <h2 style="font-family: 'Fjalla One', sans-serif;">YouTube Creation</h2>

                <p><b>Where YouTube Was Created and the History of the First Video</b></p>

                <p>YouTube's history began in San Bruno, California, where its creators lived. The company was also registered here. Initially, the platform had a non-commercial basis. But when the number of users began to reach 9 million per day, Hurley placed the first ad. But let's go back to the launch and development of the project.</p>

                <p>The history of videos on the YouTube platform began with the first uploaded video. At that time, its duration should not have exceeded 10 minutes. Jawed uploads the first video, recording his trip to the zoo. It was posted on April 23, 2005, and lasted only 18-20 seconds. Then other users began posting similar videos and sharing them.</p>

                <p>The guys understand that people need the resource for far more than just dating, and change the concept. But in the summer of 2005, it was still difficult to predict the future of the service, and Jawed leaves the company to complete his graduate studies at Stanford University.</p>

                <p>But already in the fall, the guys attract serious investors and receive $3.5 million for development. Just a year after the launch of the project, thanks to various uploaded videos, YouTube enters world history as the most popular resource.</p>

                <p>In 2006, with the platform showing the highest growth among all sites, it receives another $8 million in investment. The service already has over 65 thousand uploaded videos, and views exceed 100 million per day.</p>


                <img class="--image" src="/images/pictures/blog/youtube-story-2.png" alt="">



                <h2 style="font-family: 'Fjalla One', sans-serif;">Cooperation with Google in YouTube's History</h2>

                <p>Those who created YouTube, these young guys, in 2005 could not even imagine that by the end of next summer they would be signing contracts with various major companies. What can we say about the main deal in their life - with Google Inc. The company thought very little about buying the video hosting. Already in September 2006, it offered the platform developers $1.6 billion for their brainchild.</p>

                <p>The young people simply could not refuse such a deal and in November signed a contract, selling the video hosting to the corporation. After the deal, the newly enriched Steve Chen leaves his company, and Chad Hurley remains CEO for four years. In 2010, he will resign.</p>

                <p>Since the purchase, Google has improved the service, and in 2007 introduced a unique program allowing users to monetize their video content. The era of YouTube bloggers began. Channels for children have become especially popular. Many people of different ages have become rich thanks to this platform. Here are just some of them:</p>

