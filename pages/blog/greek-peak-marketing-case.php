                <p>The team pondered whether they could offer something similar for local residents living in the town who didn't plan to ski often. For visitors coming from afar, the lift ticket cost was just a small part of the overall expenses, including transportation, food, and accommodation. A slight price increase wouldn't likely affect their decision to visit, given the absence of alternative resorts nearby. As a result, the team developed the "10 in one" pass, which included five weekend tickets and five weekday tickets, providing a 40% discount if purchased before October 15.</p>
                <p>The "10 in one" offer quickly became popular among local residents, and this was due to several behavioral factors. Firstly, the 40% discount looked like a very lucrative deal, attracting buyers. It provided significant transactional utility. Secondly, the opportunity to purchase the pass in advance separated the payment decision from the consumption decision over time. The purchase was seen as an "investment," saving money and providing flexibility in planning skiing trips. Additionally, missed skiing opportunities were considered sunk costs, encouraging customers to use their tickets closer to the end of the season. Thus, the "10 in one" offer attracted local residents and contributed to an increase in the resort's attendance.</p>


                <img class="--image" src="/images/pictures/blog/greek-peak-2.jpg" alt="">

                <p>The "10 in one" offer was also popular because skiing is one of those activities we always plan to dedicate more time to in the coming year. "Last year, I only made it to the mountains three times; it's ridiculous considering how close 'Greek Peak' is. This year, I plan to take a few weekends and go skiing when it's less crowded." Similar to purchasing a gym membership to motivate oneself to exercise more, the identity of being a skiing enthusiast supported the idea of planning more skiing weekends in winter. The "10 in one" offer was an excellent way not only to incorporate skiing into one's schedule but also to save money.</p>

                <p>A few years later, the "6 in one," "10 in one," and seasonal passes constituted a significant portion of the resort's revenue. This income arrived quickly, relieving the management from the need to take loans to stay afloat until the start of a new season in December. Selling tickets in advance also protected against losses associated with warm, snowless winters. Although ski resorts can make snow, the air temperature must be low enough for the snow generators to operate. Furthermore, and this baffles ski resort owners, even if it's cold enough but there's no snow on the ground in town, people are much less likely to think about going skiing, even if the resort has all the conditions for it.</p>


                <p>Three years after the launch of the "10 in one" sales, Michael did the calculations and called me to report the results. Remember, this pass was sold for only 60% of the full cost of 10 tickets. "Guess how many tickets were used?" Michael asked. "Sixty percent!" "Greek Peak" sold tickets for 60% of the retail price, but only 60% of the tickets purchased this way were used. It's like selling tickets at full price but getting the money several months in advance: a tremendous benefit.</p>





                <p>Such a result seemed not to disappoint customers, most of whom bought the "10 in one" pass again the next year. Even those who didn't use all their tickets blamed themselves rather than the resort. Of course, there were those who didn't use almost any of the pre-purchased tickets by the end of the season. Some asked if they could use these tickets next year. They were politely informed that these tickets were meant for use only in the current year. However, Eli came up with a special offer for such customers. If they bought another "10 in one" pass for the next year, the validity of unused tickets was extended. While the person who managed to go skiing only 2 or 3 times last year probably wouldn't ski more than 10 times this year, the offer still looked attractive. Although I don't think there were many fools who bought another pass just to extend the validity of unused tickets, these customers liked that the resort management was accommodating and trying to restore "justice," which, as we'll soon discover, can be a crucial way to maintain customer loyalty.</p>
                <p>The "10 in one" offer turned out to be popular among local residents as well, and this can be explained by another behavioral factor. Skiing is often perceived as an activity we promise to dedicate more time to in the coming year. Many people might say to themselves, "Last year, I went to the mountains only three times, which is strange considering the proximity of 'Greek Peak.' This year, I intend to spend more weekends and go skiing, especially when the resort is less crowded." Similar to the situation with a gym membership where you plan to devote more time to physical activity, the identity of being a skiing enthusiast stimulated the idea of planning more skiing weekends in winter. The "10 in one" offer became an excellent way not only to incorporate skiing into plans but also to save money.</p>
                <p>A few years later, the "6 in one," "10 in one," and seasonal passes became a significant part of the resort's revenue. This income arrived quickly, relieving the management from the need to take loans to stay afloat until the start of a new season in December. Selling tickets in advance also protected the resort from losses associated with warm winters with little snow. Although ski resorts can make artificial snow, the air temperature must be low enough for the snow generators to operate. Furthermore, and this baffles ski resort owners, even if it's cold enough but there's no snow on the ground in town, people are much less likely to think about going skiing, even if the resort has all the conditions for it.</p>


                <p><b style="  font-size: 36px; font-family: 'Abril Fatface', serif;"> "Receiving revenue in advance—insurance against uncertainty, ensuring financial stability and independence from natural factors for the resort." </b></p>



                <p>Three years after the launch of the "10 in one" pass, Michael did the calculations and shared the results with Richard Thaler. Remember, this pass was sold for only 60% of the full cost of 10 tickets. Michael asked the question, "Guess how many tickets were used?" And the answer was, "Sixty percent!" In other words, "Greek Peak" sold discounted tickets, but ultimately, 60% of the purchased tickets were used. This effectively meant that the resort was selling tickets at full price but receiving money for them in advance. It was a tremendous win.</p>


                <p>It's amazing that such a result didn't disappoint customers. Many of them were buying the "10 in one" pass again for the next year. Even those who didn't use all their tickets blamed themselves rather than the resort. Certainly, there were those who didn't use almost any of the pre-purchased tickets by the end of the season. Some customers asked if unused tickets could be used next year. And while the answer was "no," "Greek Peak" offered a special condition for such customers. If they bought another "10 in one" pass for the next year, the validity of unused tickets was extended. Although few of them might have taken advantage of this opportunity, this offer fostered a positive perception of the resort and contributed to strengthening customer loyalty.</p>
                <p>The last pricing challenge "Greek Peak" had to tackle concerned the start of the season when, after the first snowfall, the resort opened, but only one lift was operational. Enthusiastic skiers eagerly awaited the opening since the last March and would definitely show up in the first days. What fee should be charged from them? Usually, Eli would peek out the window, assess the weather, and then inform the cashiers of the price, often giving a 50% discount from the regular rate. Of course, most skiers who came on that day had no idea what the price would be; they only knew the regular rate. Only the most meticulous could decipher Eli's pricing strategy at the beginning of the season. I call this a "secret sale." The customer approaches the counter, ready to pay the regular price, when suddenly they are told, "Today we have a 50% discount." This might contribute to increased loyalty, but on the other hand, such pricing strategy is far from the best because the buyer was already willing to pay the full price. Lowering the price makes sense only when it helps boost current sales or possibly future sales by maintaining customer loyalty.</p>



                <p><b style="  font-size: 36px; font-family: 'Abril Fatface', serif;"> "Coupons and discounts: not just a purchase incentive but also a tool for building customer loyalty." </b></p>



                <p>Michael and I came up with a new strategy. At the beginning of the season, or in our case, at any point in the season when only part of the mountain is open for skiing, the price is set by this formula. The skier pays the full price to ski on that day but receives a 50% discount coupon for the next visit, depending on how many lifts are operational. Since customers were prepared to pay the full price, this offer seemed quite generous to them, and the coupon could serve as an incentive to come again sooner or perhaps buy lunch and beer.</p>
                <p>Once, Michael told me a story that showed how popular these coupons were. One customer came to ski for the first time this season and bought a new "10 in one" pass. He's in line to get the lift ticket with this pass and suddenly hears the cashier explaining to the girl in front of him in line that she will get a 50% discount coupon, which she can use on her next visit. Our hero likes this offer so much that he hides his pass in his pocket and takes out money for a full-price single-day ticket. I've always been curious whether he used that coupon before his pass expired. We'll never know.</p>


                <img class="--image" src="/images/pictures/blog/greek-peak-4.png" alt="">


                <p>But we know for sure that creating a sustainable revenue base before the start of the season allowed the resort to achieve its goals, get out of financial troubles, and reduce dependence on snowfall during the winter. As a result, "Greek Peak" remained in business, and Richard Thaler and Michael continued their work in their respective areas.</p>
                <p>The last pricing task related to "Greek Peak" was setting prices at the beginning of the season when the resort opened after the first snowfall but operated with only one lift. Skiing enthusiasts who eagerly awaited the start of the season since March showed up in the first days. But what price should be set for them? Usually, Eli would look out the window, assess the weather, and instruct the cashiers to provide a 50% discount from the regular price. Many skiers who came on that day had no idea about the price and expected to pay the full cost. It was a kind of "secret sale." The customer would come, ready to pay the full price, and suddenly find out there's a 50% discount. This could contribute to increasing customer loyalty, but such a pricing strategy was not always the best because the buyer was already willing to pay the full price. Lowering prices makes sense only if it helps increase current sales or strengthen customer loyalty in the future.</p>




                <p>Richard Thaler and Michael developed a new strategy. At the beginning of the season, or at any other point when only part of the mountain was available for skiing, the price was set as follows. The skier paid the full price to ski on that day but received a 50% discount coupon for the next visit, depending on how many lifts were operational. Since customers were paying the full price, this offer seemed quite generous to them, and the coupon could encourage them to come back sooner or perhaps make purchases in the restaurant and have a beer.</p>



                <p><b style="  font-size: 36px; font-family: 'Abril Fatface', serif;"> "Special offers for different customer segments—a strategy based on prepayment, mental accounting, and transactional utility." </b></p>



                <p>Once, Michael told a story illustrating the popularity of these coupons. One customer came on the first day of the season to ski and bought a new "10 in one" pass. He stood in line to get the lift ticket with this pass and suddenly heard the cashier announcing to the girl in front of him in line that she would get a 50% discount on her next visit. This offer appealed so much to the customer that he hid his pass and paid the full price for a single-day ticket. We never found out if he used that coupon before his pass expired.</p>
                <p>However, it's important to note that creating a sustainable revenue base before the start of the season allowed the resort to achieve its goals, get out of financial troubles, and reduce dependence on snowfall during the winter. As a result, "Greek Peak" remained in business, and Richard Thaler and Michael continued their work in their respective areas.</p>








