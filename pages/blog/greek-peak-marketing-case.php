                <h1>Applying Behavioral Economics to Grow Revenue: A Marketing Case Study with Richard Thaler's Help</h1>



                <hr class="brief--hr">


                <br><br>
                <p>This story is about <a href="https://en.wikipedia.org/wiki/Richard_Thaler"> Nobel laureate Richard Thaler </a> and his crucial role in the marketing strategy that saved the business of the Greek Peak ski resort.

                    Richard Thaler, an expert in behavioral economics, met Michael Cobb, the brother of one of his students, and together they developed innovative solutions based on an understanding of consumer behavior.
                </p>
                <br><br>

                <img class="--image" src="/images/pictures/blog/greek-peak-1.png" alt="">



                <div style="width: 120px; float: left; ">
                    <p style="font-weight: 900; font-size: 24px; text-align: left; font-family: 'Abril Fatface', serif;"> Skiing Business Dilemma</p>
                </div>

                <p>The story begins with Richard Thaler meeting David Cobb at Cornell University, who then introduced him to his brother Michael. Michael, growing up in Ithaca, had a passion for skiing and worked as the marketing director in the family-owned company "Greek Peak," located nearby. At that time, Greek Peak faced serious financial difficulties due to consecutive seasons of insufficient snowfall and an economic crisis. The company had to take loans with high interest rates to survive until the start of the new season. In such a situation, Michael needed help, and he proposed a barter deal to Richard Thaler: lift tickets and equipment for him and his children in exchange for assistance in overcoming the company's financial troubles.</p>
                <p>It soon became evident that to overcome the crisis, Greek Peak needed to raise its service prices. However, increasing prices posed the risk of losing customers since the resort couldn't compete with larger resorts in Vermont and New Hampshire in terms of scale and size. Operational costs per customer were comparable to larger competitors, but Greek Peak only had five lifts and smaller slopes. How could they justify raising prices without losing customers, especially among students from Cornell and nearby colleges?</p>
                <p>From the perspective of behavioral economics, Greek Peak's customers assessed the lift ticket prices by comparing them to those at popular resorts in Vermont. Due to Greek Peak's smaller size, customers expected prices to be significantly lower here. However, the resort's main advantage was its accessibility. Greek Peak was the closest skiing destination in the central part of New York state, making it attractive to local residents and those coming from southern regions such as Scranton, Philadelphia, and Washington. Buses filled with skiers from these places arrived daily at Greek Peak.</p>



                <p>Even if these innovations somewhat justified the higher lift prices, there was still the question of how to deal with the local market, sensitive to price changes. In this regard, we had a ready-made model to work with. For "Greek Peak" University students, we offered a pass for six weekday tickets at a substantial discount if purchased before October 15. These passes sold well and served as a source of quick profit. I suspect students liked the offer not only for its cost-effectiveness but also because it was called "six in one." Even an implicit hint at beer always resonated with the hearts of students.</p>

                <br> <br>


                <div style="width: 120px; float: left; text-align: center">
                    <p style="font-weight: 900; font-size: 29px; text-align: center; font-family: 'Abril Fatface', serif;"> <span style="font-weight: 900; font-size: 80px; font-family: 'Abril Fatface', serif;"> 10 </span> <br> in one</p>
                </div>



                <p>We pondered whether we could offer something similar for local customers outside the student environment. Our goal was to develop an offer specifically for those living in our town, unavailable to those who only came to ski once or twice a year. For visiting skiers, the lift ticket price was a small part of the overall costs, including transportation, food, and accommodation. For them, a slight price increase wouldn't likely change the decision to go skiing, especially considering the lack of alternatives nearby. Eventually, we came up with the "10 in one" pass. It included five weekend tickets and five weekday tickets, priced 40% cheaper than the regular retail price if purchased before October 15.</p>


                <p>The "10 in one" offer enjoyed tremendous demand among the local population due to several behavioral factors. The first one is obvious: a 40% discount seems like a very lucrative deal, offering a lot of transactional utility. Secondly, purchasing in advance separates the decision to pay from the decision to consume the service, i.e., skiing. Similar to the wine example, the initial purchase is seen as an "investment" that saves money, allowing for the spontaneous decision to ski on a sunny Sunday after fresh snowfall without additional costs. The fact that someone could have chosen to dine out instead of skiing on the previous weekend doesn't register as a loss in mental accounting because the skiing was "free." For the resort, the missed skiing wasn't just free; it turned into irreversible costs. As the season approached its end, skiers were more eager to use their tickets to avoid losing the money invested in the "10 in one" pass. Additionally, each of them could bring a friend along, who had to pay the full price (tickets couldn't be transferred to others).</p>






