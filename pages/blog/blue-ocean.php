                <h1 style="font-family: 'Fjalla One', sans-serif;">Charting a Course to Blue Ocean Strategy</h1>



                <hr class="brief--hr">

                <h2>Guy Laliberté: From Street Performer to Head of Cirque du Soleil</h2>

                <p>
                    Guy Laliberté, a former accordionist, acrobat, and fire-eater, is now the head of Cirque du Soleil, one of Canada's largest cultural export companies. Founded in 1984 by a group of street performers, the company has already introduced its productions to nearly forty million people in ninety cities around the world. In less than twenty years of its existence, Cirque du Soleil began to receive the kind of profits that Ringling Bros. and Barnum & Bailey - the world champions of the circus industry - managed to achieve only after more than a hundred years after their appearance. This rapid growth is also remarkable in that it occurred not in an attractive, but in a fading industry, where a traditional strategic analysis indicated limited growth opportunities.
                </p>



                <img img style="width: 100%;  margin-right:  10px; padding-bottom: 10px " src="/images/pictures/blog/blue-ocean.png" alt="">





                <h2>Creating a New Market Segment</h2>

                <p>
                    Another interesting aspect of Cirque du Soleil's success was that the company did not win by poaching customers from the declining circus industry, which was historically focused on children. Cirque du Soleil did not compete with Ringling Bros. and Barnum & Bailey. Instead, the company created a new, untapped market segment, free of competitors. It targeted an entirely new group of consumers: adults and corporate clients willing to pay several times more than a regular circus ticket to see a new, unprecedented show. The name of one of Cirque du Soleil's first projects spoke for itself: "We are reinventing the circus from scratch."
                </p>





                <h2>Blue Oceans vs. Red Oceans</h2>

                <p>
                    Cirque du Soleil succeeded because it understood that in order to win in the future, companies should stop competing with each other. The only way to beat the competition is to stop trying to beat it. To understand what Cirque du Soleil achieved, imagine a market universe consisting of two oceans: red and blue. The red oceans symbolize all existing industries at the moment. This is the known part of the market. Blue oceans denote all industries that do not yet exist today. These are unknown areas of the market.
                </p>

                <p>
                    In red oceans, industry boundaries are defined and agreed upon, and the rules of the competition game are known to all. Here, companies seek to outperform their rivals in order to attract a greater share of existing demand. As the market becomes more and more crowded, the chances for growth and profit making keep falling. Products become commodities, and ruthless competitors cut each other’s throats, turning the red ocean blood red.
                </p>

                <p>
                    In contrast, blue oceans denote untouched market areas which demand creative approaches and offer opportunities for growth and high profits. Although some blue oceans emerge beyond the established industry boundaries, most blue oceans emerge within red oceans by expanding the existing industry boundaries, as Cirque du Soleil did. In blue oceans, competition is irrelevant because the rules of the game have yet to be set.
                </p>

                <h2>Creating Blue Oceans</h2>

                <p>
                    Cirque du Soleil succeeded because it understood that in order to win in the future, companies should stop competing with each other. The only way to beat the competition is to stop trying to beat it. This book offers you practical patterns and analytical tools for systematically finding and capturing blue oceans. Unfortunately, blue ocean maps are almost non-existent. All major developments in strategic thinking over the past twenty-five years have focused mainly on competition-based red ocean strategies. As a result, we know a lot about battling competitors in red waters, from analyzing the underlying economic structure of an industry and choosing strategic positions - low cost, differentiation, or focus - to benchmarking competitors. Debates on blue oceans continue without interruption. However, there is very little practical guidance on how to create such oceans.
                </p>

                <h2>The Evolution of Industries</h2>

                <p>
                    Blue oceans have always been created. Although the term blue oceans is quite new, the same cannot be said about the oceans themselves. They are an integral part of the business world of the past and present. Look at the world a hundred years ago and ask yourself: how many of today's industries were completely unknown back then? The answer is that such fundamental industries as automotive, sound recording, aviation, oil refining, healthcare, and management consulting were either unheard of or just beginning to emerge at best.
                </p>

                <p>
                    Fast forward just thirty years. Again, you can list dozens of multibillion-dollar industries such as mutual funds, mobile phones, gas power plants, biotechnology, retail discounters, express couriers, minivans, snowboards, coffee bars, and home video recorders - and that's far from all of them. Just three decades ago, not one of these industries really existed. Now let’s move the clock forward twenty or fifty years and ask ourselves: How many still unknown industries will there be then? If history is any predictor of the future, the answer is many.
                </p>

                <p>
                    The truth is industries have never been static. They continuously evolve. Their performance improves, markets expand, and players come and go. History makes it clear that we have seriously underestimated the possibility of creating new industries and re-creating existing ones. However, strategic thinking still focuses mainly on red ocean strategies related to competition.
                </p>





                <h2>Quantifying the Impact of Blue Oceans on Profit and Revenue</h2>

                <p>
                    In the course of studying the business initiatives of 108 organizations, we tried to quantify the impact of blue oceans on profit and revenue (Fig. 1.1). It turned out that 86% of initiatives represented a linear expansion, that is, they involved gradual improvements in the existing market space - within the red oceans. They accounted for only 62% of total revenue and 39% of total profit.
                </p>

                <p>
                    The remaining 14% of initiatives were aimed at creating blue oceans. They generated 38 and 61%, respectively. Given that business initiatives included all investments in creating red and blue oceans (regardless of the size of the revenues and profits they generate, including completely failed projects), the benefits of creating a blue ocean are obvious. And although we do not have data on the success rate of initiatives in red and blue oceans, the above overall differences in their effectiveness speak for themselves.
                </p>

