                <h2>The Growing Need to Create Blue Oceans</h2>

                <p>
                    Behind the growing need to create blue oceans lies a number of driving forces. Technological advances have greatly increased production efficiency and allowed suppliers to produce unprecedented volumes of products and services. As a result, supply exceeds demand in many industries. Globalization exacerbates the situation. As national and regional borders fade and information about products and prices spreads instantly around the world, niche markets and monopolistic domains continue to disappear. Supply is increasing due to global competition, but there is no evidence of increasing demand worldwide, and statistics even point to declining numbers of participants in many developed markets. The result has been an ever-increasing commoditization of goods and services, more severe price wars, and declining profits.
                </p>

                <p>
                    Recent research on major American brands within an industry confirmed this trend. According to the research, brands in major product and service categories are becoming more and more similar, and as they become more similar, people are increasingly making their choice based on price. Unlike the past, the consumer is no longer willing to wash only with "Tide." And he will not cling to "Colgate" if "Crest" toothpaste is on sale at discounted prices, and vice versa. In areas crowded with manufacturers, it is becoming increasingly difficult to distinguish between brands in both economic upturns and downturns. All this means that the business environment that gave rise to most strategic and managerial approaches in the 20th century is gradually disappearing. More and more blood is being spilled in the red oceans, and managers need to pay more attention to the blue oceans than the hordes of current managers are used to.
                </p>

                <h2>From Company and Industry to Strategic Move</h2>

                <p>
                    How can a company break out of the red ocean of fierce competition? How can it create a blue ocean? Is there a systematic approach that can ensure that a company achieves this goal and thus maintains high performance? When we started looking for answers, our first step was to define the basic unit of analysis for our research. To understand where high performance comes from, the company is usually used as the main unit of analysis in business literature. People do not cease to admire how companies achieve significant growth and profitability rates, having an exquisite set of strategic, operational and organizational characteristics.
                </p>

                <p>
                    However, we asked a different question: are there any "exceptional" or "visionary" long-lived companies that constantly outplay the market and create blue oceans over and over again? Take, for example, the books "In Search of Excellence" and "Built to Last." The bestseller "In Search of Excellence" was published twenty years ago. However, two years after its publication, some of the companies studied by the author - Atari, Cheseborough-Pond's, Data General, Fluor, National Semiconductor - faded into oblivion. As stated in Managing on the Edge, two-thirds of the exemplary companies listed in the book lost their industry leadership position within five years of the book's publication. The book "Built to Last" continued the same theme. In it, the author sought to identify the "success habits of visionary companies" that had long experience of high performance. However, to avoid the mistakes made in "In Search of Excellence", the research period in "Built to Last" was expanded to the size of the company's life cycle, and only companies that had existed for at least forty years were analyzed. "Built to Last" also became a bestseller. However, upon close examination, some flaws emerged in the supposedly visionary companies examined in the book.
                </p>

                <p>
                    As shown in "Creative Destruction," much of the success that the author of "Built to Last" attributed to exemplary companies was more the result of the industry as a whole rather than the fruits of the companies themselves. For example, Hewlett-Packard (HP) met the criteria set out in "Built to Last" because it had long been ahead of the entire market. In practice, the entire computer components industry was ahead of the entire market along with HP. Moreover, HP did not even surpass all of its competitors within that industry. Noting this and other examples, the authors of "Creative Destruction" wondered if there were ever any truly "visionary" companies that had long been ahead of the entire market.
                </p>

                <img img style="width: 100%;  margin-right:  10px; padding-bottom: 10px " src="/images/pictures/blog/blue-ocean-2.png" alt=" blue ocean vs red ocean ">




                <h2>The Strategic Move: Key to Blue Oceans and High Performance</h2>

                <p>
                    In addition, we all had the opportunity to observe the stagnation or decline of Japanese companies, which at the height of their heyday in the late 1970s and early 1980s were renowned as revolutionary strategists. If there are no eternally high-performing companies and if the same company goes from unprecedented success to decline, it turns out that the company cannot be considered a suitable unit of analysis in studying the sources of high performance and blue oceans.
                </p>

                <p>
                    As mentioned above, history also shows that industries have never ceased to emerge and expand and that industry conditions and boundaries are not constant; they are set by individual entities. Companies do not need to butt heads in the space of an industry; Cirque du Soleil created a new market space in the entertainment sector and, as a result, achieved powerful profit growth. It turns out that neither the company nor the industry can be considered the optimal unit of analysis for studying the sources of profitable growth.
                </p>

                <p>
                    In full accordance with this, our research showed that it is the "strategic move" - not the "company" or "industry" - that is the appropriate unit for explaining the creation of blue oceans and the permanent high performance of companies. A strategic move is a set of actions and decisions by management to develop a major business offer that opens up a new market. For example, in 2001 Compaq was acquired by Hewlett-Packard and lost its independence. As a result, many labeled the company as a failure. However, this did not devalue Compaq's blue ocean-focused strategic move to shape the server industry. These strategic moves not only became part of the company's powerful return to the market in the mid-1990s but also paved the way for a new multi-billion dollar market in computer hardware manufacturing.
                </p>

                <p>
                    Appendix A, "A Historical Overview of the Blue Ocean Creation Pattern," provides a condensed history of three quintessentially American industries, based on our database: the automotive industry - how we get to work; the computer industry - what we use at work; the film industry - where we go to entertain ourselves after work. As shown in Appendix A, there are no consistently successful companies or industries. However, the strategic moves that led to the creation of blue oceans and new trajectories of powerful profit growth are strikingly similar.
                </p>

                <p>
                    The strategic moves we are talking about - moves to create products and services that opened up and conquered new market spaces with sharply increased demand - are exciting stories of profitable growth, stories that make you think about the opportunities missed by those companies stuck in red oceans. We built our research around these strategic moves to understand how blue oceans are created and high business performance is achieved. We studied over one hundred and fifty strategic moves made between 1880 and 2000 in more than thirty industries and meticulously examined the business players involved in each of these events. The industries were extremely diverse: from hotels, film, retail, air travel, energy, computers, broadcasting and construction to automotive and steel.
                </p>

                <p>
                    We analyzed not only the winners who managed to create blue oceans, but also their less successful competitors. Both within each specific strategic move and across all strategic moves, we looked for similarities within the blue ocean creator group and among the less fortunate players stuck in the red ocean. We also looked for differences between these two groups. In the process, we tried to identify the common factors that led to the creation of blue oceans, as well as the key characteristics that distinguish the winners from the mere "survivors" and failures adrift in the red ocean.
                </p>

                <p>
                    As a result, after studying more than thirty industries, we found that neither industry characteristics nor organizational characteristics can explain the differences between these two groups. In assessing variable industry, organizational and strategy characteristics, we found that small and large companies succeeded in creating and capturing blue oceans, as did young and old managers, companies from attractive and unattractive industries, newcomers and dinosaurs, private and state-owned companies, representatives of low-tech and high-tech industries, as well as companies of very diverse national origins. Our analysis did not reveal a single permanently flawless company or industry. However, behind the seeming variety of success stories, we still managed to find a consistent and universal set of strategic moves to create and capture blue oceans.
                </p>

                <p>
                    Whether it was Ford in 1908 with its Model T; GM, which launched elegant emotionally appealing cars in 1924; CNN, which offered real-time twenty-four hour seven day a week news in 1980; or Compaq, Starbucks, Southwest Airlines or Cirque du Soleil, any other blue ocean cases we studied in our research, the approach to blue ocean strategy was always similar, regardless of industry.
                </p>

                <p>
                    Our research also covered famous strategic moves that led to radical changes in the public sector. And here too we discovered an amazingly similar pattern of action.
                </p>


                <h2>Revolutionizing the Circus Industry</h2>

                <p>
                    Just as when the circus industry engaged in star wars, the so-called stars of the circus were pale in comparison to movie stars in the minds of viewers. And again, this tactic was very costly and had practically no impact on the audience. The idea of three rings also faded away. Such shows not only created tension for viewers forced to quickly shift their gaze from one ring to another, but also required a larger number of performers, leading to higher costs. Renting out premises for retail outlets and selling goods between rows seemed like a good way to make a profit, but in practice it turned out that the inflated prices kept viewers from making purchases and left them feeling cheated.
                </p>

                <h2>The Essence of Circus Appeal</h2>

                <p>
                    All the perennial appeal of the circus boiled down to three main factors: the big top, clowns, and classic circus acts of various kinds of acrobatics and unicycling. Therefore, Cirque du Soleil retained clowns, but their humor became more subtle and refined rather than vulgar. The big top was refined, yet it was the big top that, ironically, many circuses had abandoned in favor of renting venues.
                </p>

                <h2>Revitalizing the Big Top Symbolism</h2>

                <p>
                    Seeing that the big top alone was the symbol that embodied the magic of the circus, Cirque du Soleil created a classic circus symbol distinguished by its magnificent exterior finish and increased comfort. Such tents evoked thoughts of the great and legendary circuses. Sawdust and hard benches were removed. The acrobatic and other breathtaking acts remained but began to play a smaller role. They were made more elegant with an artistic flair and intellectual component added.
                </p>

                <h2>Infusion of Theatrical Elements</h2>

                <p>
                    Looking into the market territory of theater, Cirque du Soleil also introduced new non-circus elements such as a plot line and with it the intellectual richness, artistic music and dance, and diversity of performances. By incorporating all these components, which were an innovation in the circus industry, into its program, Cirque du Soleil managed to create more sophisticated performances. Moreover, by introducing the concept of a variety of performances and thus giving people a reason to go to the circus more often, Cirque du Soleil significantly increased existing demand.
                </p>

                <h2>An Unprecedented Value Proposition</h2>

                <p>
                    In summary, Cirque du Soleil offers the best of circus and theater, while minimizing or eliminating everything else. Thanks to this unprecedented value proposition, Cirque du Soleil created a blue ocean and invented a new kind of live entertainment that was markedly distinct from both the traditional circus and the traditional theater. At the same time, by abandoning many of the most expensive components of the circus, the company managed to sharply reduce its costs, thereby achieving both differentiation and low costs simultaneously. Cirque du Soleil took a strategic step by bringing its ticket prices closer to those of the theater. Ticket prices were several times higher than the level accepted in the circus industry, but they were attractive to adult viewers accustomed to theater ticket prices.
                </p>

                <h2>The Dwindling Impact of Circus Stars</h2>

                <p>
                    Similarly, when the circus industry started inviting stars, the so-called circus stars seemed quite insignificant compared to movie stars in the minds of viewers. And again, this tactic required major expenses and had practically no impact on the audience. The idea of three rings also faded away. Such shows not only created tension for viewers forced to quickly shift their gaze from one ring to another, but also required more performers, leading to higher costs. Renting out premises for retail outlets and selling goods between rows seemed like a good way to make a profit, but in practice it turned out that the inflated prices kept viewers from making purchases and left them feeling cheated.
                </p>

                <h2>The Essence of Circus Appeal: Big Tops, Clowns, and Classic Acts</h2>

                <p>
                    All the perennial appeal of the circus boiled down to three main factors: the big top, clowns, and classic circus acts of various kinds of acrobatics and unicycling. Therefore, Cirque du Soleil retained clowns, but their humor became more subtle and refined rather than vulgar. The big top was refined, yet it was the big top that, ironically, many circuses had abandoned in favor of renting venues. Seeing that the big top alone was the symbol that embodied the magic of the circus, Cirque du Soleil created a classic circus symbol distinguished by its magnificent exterior finish and increased comfort. Such tents evoked thoughts of the great and legendary circuses. Sawdust and hard benches were removed.
                </p>

                <h2>Evolution of Circus Acts: Elegance, Artistry, and Intellectual Components</h2>

                <p>
                    The acrobatic and other breathtaking acts remained but began to play a smaller role. They were made more elegant with an artistic flair and intellectual component added. Looking into the market territory of theater, Cirque du Soleil also introduced new non-circus elements such as a plot line and with it the intellectual richness, artistic music and dance, and diversity of performances. All these elements, which were an innovation in the circus industry, were taken from the alternative live entertainment industry - the theater business. For example, if in traditional circuses the acts are just a series of performances unrelated to each other, then each Cirque du Soleil creation, on the contrary, had a theme and plot, thereby resembling a theatrical performance to some extent. Although the theme was set vaguely (this was done deliberately), it brought harmony and an element of intellect to the show without limiting the number of circus acts themselves.
                </p>

                <h2>Borrowing from Broadway: Variety, Music, and Dance</h2>

                <p>
                    In addition, Cirque du Soleil borrowed some ideas from Broadway shows. For example, it presents a variety of programs and productions, not traditional “one size fits all” shows. Again, following the example of Broadway shows, each Cirque du Soleil performance has appropriate musical accompaniment that governs (rather than overrides) the visual component, lighting and duration of each performance. The program also includes beautiful dances - the idea was taken from theater and ballet. By incorporating all these components into its program, Cirque du Soleil managed to create more sophisticated performances. Moreover, by introducing the concept of a variety of productions and thus giving people a reason to go to the circus more often, Cirque du Soleil significantly increased existing demand.
                </p>

                <h2>Summary: Cirque du Soleil's Unprecedented Value Proposition</h2>

                <p>
                    In summary, Cirque du Soleil offers the best circus and theater has to offer, while minimizing or eliminating everything else. Thanks to this unprecedented value proposition, Cirque du Soleil created a blue ocean and invented a new kind of live entertainment that was markedly distinct from both the traditional circus and the traditional theater. At the same time, by abandoning many of the most expensive components of the circus, the company managed to sharply reduce its costs, thereby achieving both differentiation and low costs simultaneously. Cirque du Soleil took a strategic step by bringing its ticket prices closer to those of the theater. Ticket prices were several times higher than the level accepted in the circus industry, but they were attractive to adult viewers accustomed to theater ticket prices.
                </p>


                <h2>Developing and Executing a Blue Ocean Strategy</h2>


                <p>

                    Although economic conditions indicate an increasing need to create blue oceans, there is a persistent belief that the chances of success decrease when a company goes beyond the existing industry space in its activities. How to succeed in blue oceans? What should companies do to maximize opportunities while minimizing the risks associated with developing and implementing a blue ocean strategy? If you do not have a good enough understanding of the principles of expanding opportunities and minimizing risks that underlie the creation and conquest of blue oceans, your chances of failure in creating a blue ocean will be high.
                </p>


                <p>

                    Of course, there is no such thing as a risk-free strategy. Strategy always involves opportunities and risks - regardless of whether events occur in a red or blue ocean. However, at present, the balance on the playing field is disrupted in favor of tools and analytical frameworks designed to succeed in red oceans. Until this changes, red oceans will continue to prevail in a company's strategic plans, even if the imperative to create blue oceans becomes even more obvious. Perhaps this is why, despite repeated calls for companies to move beyond the existing industry space, they have not taken any serious steps in this direction.

                </p>



