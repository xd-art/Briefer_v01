                <h1 style="font-family: 'Fjalla One', sans-serif;">The Problem of Aligning Artificial Intelligence Goals with Human Ones</h1>



                <hr class="brief--hr">


                <p>In his book <a href="https://en.wikipedia.org/wiki/Life_3.0" target="_blank"> <b>"Life 3.0: Being Human in the Age of Artificial Intelligence,"</b></a> renowned physicist <a href="https://en.wikipedia.org/wiki/Max_Tegmark" target="_blank"> Max Tegmark </a> raises an important question about how to ensure alignment of artificial intelligence goals with human ones. This problem is key to creating so-called "friendly" AI that will be safe and beneficial to humans.</p>


