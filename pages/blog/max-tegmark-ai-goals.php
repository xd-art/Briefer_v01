                <p>In the excerpt below, the author analyzes various aspects of this problem, including an AI's understanding of human goals, adoption of these goals, and retention of them during self-learning and self-improvement. Tegmark points out the complexity of each of these components and argues that to safely create a superintelligent AI, solutions to this multifaceted problem must be found in advance, before its emergence.</p>
                <p>What follows is Tegmark's opinion on this issue. It gives an idea of the depth of the problems arising on the path to creating friendly and safe artificial intelligence.</p>






                <h2>The Challenge of Aligning Goals</h2>

                <p>As machines become smarter and more powerful, it becomes increasingly important that their goals do not conflict with ours. Currently, the machines we build are somewhat dull, and the question is not whether human goals will ultimately prevail, but how much trouble these machines will cause humanity before we align their goals with ours. However, if superintelligent artificial intelligence ever emerges, the roles will reverse. Since intelligence is the ability to achieve goals, artificial superintelligence, by definition, is much better at achieving its goals than humans are at achieving theirs. Hence, its goals will ultimately prevail over ours.</p>



                <h2>The Ingenuity of Artificial Intelligence</h2>

                <p>If you want to experience what it feels like when a machine stands in your way, simply download the cutting-edge chess simulator and try to compete with it. You will never win, and it quickly brings in others to replace it. In other words, the real danger of artificial intelligence lies not in its malevolence but in its ingenuity. A superintelligent artificial intelligence will effectively pursue its goals, and if its goals conflict with ours, we will be in a precarious position.</p>

                <h2>The Importance of Friendly AI</h2>

                <p>Most researchers believe that if we ever create superintelligence, we must ensure that it is "friendly," in the words of one of the creators of the theoretical approach to artificial intelligence safety, Eliezer Yudkowsky. This means that its goals should not contradict ours. Understanding how to align the goals of superintelligent artificial intelligence with ours is not only important but also challenging. In fact, it is an unsolved problem at the moment, divided into three challenging sub-problems actively studied by computer scientists and researchers from other fields.</p>

                <h3>The Three Sub-Problems</h3>

                <p> <b> These sub-problems include:</b></p>
                <ol>
                    <li>Getting artificial intelligence to understand our goals</li>
                    <li>Getting artificial intelligence to adopt our goals</li>
                    <li>Getting artificial intelligence to adhere to our goals</li>
                </ol>
                <p>Let's delve into each sub-problem, deferring the question of what is meant by "our goals" to the next section.</p>


