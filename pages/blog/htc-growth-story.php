                <h1>HTC's Meteoric Growth from PDA Maker to Apple Rival</h1>
                <h2 class=""> <b>&#x2022 From Startup to Android Trailblazer in 13 Years
                        The Innovative Spirit That Transformed HTC into an Industry Leader</b> </h2>



                <hr class="brief--hr">

<br><br>
                <img class="--image" src="https://upload.wikimedia.org/wikipedia/commons/thumb/d/db/Cher_Wang_in_WEF.jpg/1280px-Cher_Wang_in_WEF.jpg" alt="">

                <p>
                    <b style="font-size: 33px;">"We started with the desire to give people around the world the ability to feel the full power of computers whenever and wherever they need it,"</b> <br>  says <a href="https://www.teilfirms.com/blog/who-is-cher-wang">  Cher Wang</a>, Chairman of HTC. <br><br>Just five years ago, no one could imagine that companies from Asia would compete in high-tech markets: Chinese Huawei and ZTE would take contracts from Nokia Siemens Network, Ericsson and Alcatel-Lucent in telecommunications; CNOOC in oil field development; Comac in aircraft manufacturing; and Geely and BYD in the automotive market. Especially fierce competition is now observed in the IT market, in the field of smartphone and component manufacturing. Taiwanese corporation HTC today releases its new products no later than, and even earlier than Apple. But how did this all happen?
                </p>




                <p>
                    13 years ago, when the company was still a small startup, its founder Cher Wang, daughter of one of the richest entrepreneurs in Taiwan, Wang Yung-Ching, set out to make people's lives easier with mobile communications. She wanted everyone to be able to freely talk with friends and family, read, listen to music and have Internet access - all from one device. But seeing an open niche in the market is one thing, and getting consumers to buy your innovative product is quite another. The PDA failed to capture the imagination of the entire corporate world, so two years later the company experienced a liquidity crisis. <b>"The market just wasn't ready for PDA phones that could be used as mini computers,"</b> recalls Ms. Wang. However, the indomitable entrepreneur, who was also the head of Via Technologies Inc., a chip design company, refused to go back. She realized that network operator customers like Vodafone need functionality and will pay for it. <br><br> <b style="font-size: 33px;">"I always believed that if I use some gadget myself, then many more people will also love it,"</b> says Mrs. Wang. <br><br> <b style="font-size: 33px;">"People don't want to carry around a bunch of devices everywhere: a personal assistant, music player, phone, camera and pager."</b><br><br> This is where she saw an opportunity for HTC: to design a phone and software that allows all programs to work smoothly, which will ultimately give the company an edge over larger competitors such as Apple, Nokia, and Motorola. Therefore, additional investments in the company to increase its design and engineering capabilities exceeded six digits.
                </p>

                <p>
                    By 2000, HTC had won a contract to produce the then very popular iPAQ pocket PCs for Compaq, which was acquired by Hewlett-Packard in 2002. HTC's reputation as a phone designer and manufacturer grew like a snowball as international operators like T-Mobile International and Cingular Wireless joined in. A key partnership for the company was with Microsoft Corp. to install mobile phone technologies for Windows. As Cher Wang recalls today, she flew to Seattle annually to meet with Bill Gates and Steve Ballmer, as the partnership with Microsoft was very important for the company at the time. It is worth noting that it continues to this day. <b>"They are innovators who can push boundaries, who were going to work long-term and were willing to invest for several years,"</b> says Microsoft's Vice President of Sales Sanjay Jha. <b>“Bill was impressed with their investment.”</b>
                </p>

                <p>

                <img style="width: 200px; float:left;;" src="/images/pictures/blog/htc-1.png" alt="">

                    The results were quick to arrive. Net profit for the first nine months of 2005 increased 175% over the previous year (to $200 million), sales - by 200% (to $1.3 billion). Share prices tripled, making HTC one of the most efficient companies in Asia. The company was making smartphones for iMate, O2, XDA, and even Palm. However, 2006 was a turning point in its history. HTC ceased to be a faceless phone manufacturer, becoming a corporation that promotes phones under its own brand, as well as premium offerings that dramatically changed the structure of the entire business. By 2008, one in six smartphones in the US was made under the HTC brand. As the company president noted at the time, many more Americans are already using their phones, they just don't know it yet.
                </p>

              


                <p>
                    Soon HTC announces a long-term partnership with Google, switching to producing phones with the pre-installed Android OS instead of Windows Mobile, thereby partially turning away from Microsoft. <b>"When we started working with Google, we had no clear vision,"</b> notes Cher Wang. <b>“The Android platform probably would never have become known. And if it had, it would have been just another platform on the market. But we got excited and started working on it.”</b> Together with Google, HTC released the G1 on T-Mobile and the already heart-winning Nexus One. One of the latest news: HTC bought the French startup Abaxia to strengthen its market position. Since the latter is engaged in developing solutions that help quickly promote and monetize the operator and device manufacturer, we can say that HTC is keeping up with its main competitor Apple and plans to enter the mobile advertising market.
                </p>

                <p>
                    Today the company has about 9,000 employees worldwide, its revenues last year amounted to $4.2 billion, R&D spending - $643 million. But HTC does not stop there and assures that in the next three years it intends to build a new R&D center in the suburbs of Taipei, for which it will spend about $1 billion. Over the past four years, the corporation has been working to create a brand and its unique values. <b>“Brand value is like respect, you have to earn it,”</b> says HTC CEO Peter Chou. <br><br> <b style="font-size: 33px;" >“You can't buy respect. You can spend all the money to get known, but it doesn't mean anything. I want HTC's brand to have huge experience behind it."</b><br><br> Having long-term partnerships with Google and not giving preference to any one mobile operator (as Apple did), HTC leaves itself a fairly wide field of action. Work is underway to create its own operating system, and the joint tablet computer launch with Google is just around the corner.
                </p>

                <p>
                    Today, thanks to its innovation, the HTC Corporation is constantly expanding its range of devices, introducing new versions that support specialized applications for the diverse needs of its customers and partners. The company's product line offers easy-to-use solutions covering the entire spectrum of mobile multimedia and wireless internet on the go anytime. HTC has a long list of "First" products and developments:
                </p>

                <ul>
                    <li>First color palmtop computer (1999)</li>
                    <li>First Microsoft pocket computer (2000)</li>
                    <li>First Microsoft wireless pocket computer (2002)</li>
                    <li>First Microsoft powered Smartphone (2002)</li>
                    <li>First Microsoft Smart Music Phone (2004)</li>
                    <li>First large 2.8 "touchscreen TFT LCD display</li>
                    <li>First Microsoft 3G smartphone (2005)</li>
                    <li>First phone on the Microsoft Windows Mobile 5.0 platform (2005)</li>
                    <li>First tri-band UMTS 3G device on the Microsoft Windows Mobile platform (2006)</li>
                    <li>First smartphone on the Microsoft Windows 5.0 platform (2006)</li>
                    <li>First tri-band UMTS PDA</li>
                    <li>First intuitive touchscreen controlled by fingertips (June 2007)</li>
                    <li>First 4G Android phone (March 2010)</li>
                </ul>









