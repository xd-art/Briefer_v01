                    <li><strong>SEO (Search Engine Optimization):</strong> Optimization of content for search engines, contributing to improved brand visibility in search results.</li>

                    <a class="chips" target="_blank" href="/pages/marketing/seo.php"> How to write SEO brief </a>

                    <li><strong>Social Media:</strong> Active interaction with the audience through social platforms, aiming to create a community and distribute content.</li>
                    <li><strong>Email Marketing:</strong> Use of personalized and targeted email campaigns to maintain interaction with customers and inform them about promotions, news, and offers.</li>
